create table skill_basic(
                           id bigint not null auto_increment PRIMARY KEY,
                           class_skill_id bigint not null,
                           skill_level int not null,
                           damage int,
                           hit_chance int,
                           mana_cost int,
                           stamina_cost int,
                           rounds_time int,
                           effect_rounds_time int,
                           additional_effect_chance int,
                           special_effect_description varchar(50),
                           special_effect_value int,
                           skill_difficulty varchar(32),
                           constraint fk_skill_basic_class_skill_id foreign key (class_skill_id) references class_skill(id)
);

